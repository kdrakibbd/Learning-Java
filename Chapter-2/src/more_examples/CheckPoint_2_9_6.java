package more_examples;

public class CheckPoint_2_9_6 {
    public static void main(String[] args) {
        System.out.println("25 / 4 is : " + 25 / 4); // 6
        System.out.println("25 / 4.0 is : " + 25 / 4.0); // 6.25
        System.out.println("3 * 2 / 4 is : " + 3 * 2 / 4); // 1 cz left to right association
        System.out.println("3.0 * 2 / 4 is : " + 3.0 * 2 / 4); //3.0 left to right association
    }
}
